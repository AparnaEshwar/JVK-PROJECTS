package ForloopStatements;

public class PrintingStarEven {
	public static void main(String[] args) {
		for(int Star=0; Star<=7; Star++) {
			System.out.print("*");
	}
	System.out.println();// print next line 
	for(int Star1=0; Star1<=7; Star1++) {
		System.out.print("*");
	}
         
 
}
}
